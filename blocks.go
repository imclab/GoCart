func Lighten(c color.RGBA, v byte) color.RGBA {
	var r, g, b byte
	if c.R >= 223 {
		r = 0xFF
	} else {
		r = c.R + v
	}
	
	if c.G >= 223 {
		g = 0xFF
	} else {
		g = c.G + v
	}
	
	if c.B >= 223 {
		b = 0xFF
	} else {
		b = c.B + v
	}
	return color.RGBA{r, g, b, c.A}
}

